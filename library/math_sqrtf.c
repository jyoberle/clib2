static	const float	one	= 1.0, tiny=1.0e-30;

// If x is NaN, a NaN shall be returned.
// If x is ±0 or +Inf, x shall be returned.
// If x is -Inf, a domain error shall occur, and a NaN shall be returned.
// For finite values of x < -0, a domain error shall occur, and  either a NaN (if supported), or an implementation-defined value shall be returned.
float
sqrtf(float x)
{
	volatile float z;	/* to prevent GCC from optimizing it away */
	LONG	sign = (LONG)0x80000000U; 
	ULONG r;
	LONG ix,s,q,m,t,i;

	if(isinf(x))
	{
		// If x is ±0 or +Inf, x shall be returned.
		if(signbit(x) == 0)
		{
			return(__inff());
		}
		else
		{
			// If x is -Inf, a domain error shall occur, and a NaN shall be returned.
			__set_errno(EDOM);
			return(nanf(NULL));
		}
	}

	GET_FLOAT_WORD(ix,x);

    /* take care of Inf and NaN */
	if((ix&0x7f800000)==0x7f800000) {			
	    return x*x+x;		/* sqrt(NaN)=NaN, sqrt(+inf)=+inf
					   sqrt(-inf)=sNaN */
	} 
    /* take care of zero */
	if(ix<=0) {
	    if((ix&(~sign))==0) return x;/* sqrt(+-0) = +-0 */
	    else if(ix<0)
		return (x-x)/(x-x);		/* sqrt(-ve) = sNaN */
	}
    /* normalize x */
	m = (ix>>23);
	if(m==0) {				/* subnormal x */
	    for(i=0;(ix&0x00800000)==0;i++) ix<<=1;
	    m -= i-1;
	}
	m -= 127;	/* unbias exponent */
	ix = (ix&0x007fffff)|0x00800000;
	if(m&1)	/* odd m, double x to make it even */
	    ix += ix;
	m >>= 1;	/* m = [m/2] */

    /* generate sqrt(x) bit by bit */
	ix += ix;
	q = s = 0;		/* q = sqrt(x) */
	r = 0x01000000;		/* r = moving bit from right to left */

	while(r!=0) {
	    t = s+r; 
	    if(t<=ix) { 
		s    = t+r; 
		ix  -= t; 
		q   += r; 
	    } 
	    ix += ix;
	    r>>=1;
	}

    /* use floating add to find out rounding direction */
	if(ix!=0) {
	    z = one-tiny; /* trigger inexact flag */
	    if (z>=one) {
	        z = one+tiny;
		if (z>one)
		    q += 2;
		else
		    q += (q&1);
	    }
	}
	ix = (q>>1)+0x3f000000;
	ix += (m <<23);
	SET_FLOAT_WORD(z,ix);
	return z;
}

